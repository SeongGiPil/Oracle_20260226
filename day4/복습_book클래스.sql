--날짜별 매출
--날짜,해당의 매출,매출이 20000이상일경우 '상',10000이상일 경우'중',그외는 '하'로표기


SELECT *
ORDER DATE 
SUM(SALEPRICE)
FROM ORDERS;
CASE 
WHEN  SUM (SALPRICE)=>20000THEN'상'
WHEN  SUM (SALPRICE)=>10000THEN'중'
ELSE '하'
END 상중하
FROM ORDERS 
GROUP BY ORDERDATE;








    