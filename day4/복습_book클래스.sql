    SELECT 날짜,SUM_PRICE
    FROM(
    SELECT TO_CHAR(ORDERDATE,'YYYY/MM/DD')AS 날짜,SUM(SALEPRICE)
    AS SUM_PRICE,1 AS ORDERKEY
FROM ORDERS
GROUP BY ORDERDATE
UNION
SELECT '매출총액' AS 날짜,
SUM(SALEPRICE) AS SUM_PRICE,
2 AS ORDERKEY
FROM ORDERS
)
ORDER BY ORDERKEY, SUM_PRICE DESC;



