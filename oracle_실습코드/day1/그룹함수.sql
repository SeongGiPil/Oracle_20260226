FROM PROFESSOR;


SELECT AVG(PAY)
FROM PROFESSOR;


SELECT MAX(PAY)
FROM PROFESSOR;

SELECT MIN(PAY)
FROM PROFESSOR;


SELECT COUNT(*),COUNT
(BONUS)
FROM PROFESSOR;


SELECT POSITION, AVG(PAY)
FROM PROFESSOR
GROUP BY POSITION;



SELECT STU_DEPT,STU_GENDER,AVG(STU_HEIGHT)
FROM STUDENT
WHERE STU_GENDER IS NOT NULL
GROUP BY STU_DEPT,STU_GENDER;


SELECT POSITION,MAX(PAY)
FROM PROFESSOR
GROUP BY NAME,POSITION;

--그룹함수에서의 조건
