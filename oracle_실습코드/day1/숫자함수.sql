--숫자함수
--1)Round:반올림
SELECT ROUND( 123.45678,2)
FROM DUAL;

--2)CEIL:올림
SELECT CEIL(123.001)
FROM DUAL;
--3)FLOOR:내림
SELECT FLOOR(123.999)
FROM DUAL;

--4.TRUNK:특정위치까지 출력
SELECT TRUNC(123.4567,2)
FROM DUAL;

--5)MOD:나머지값
SELECT STU_NAME,MOD(STU_HEIGHT,2)
FROM STUDENT;

--6)SIGN:숫자 부호를 반환->양수면 1 음수면2
SELECT SIGN(10) AS ZZ
FROM STUDENT;

--7) ABS:절대값
SELECT ABS(-10)
FROM DUAL;

--8)POWER:제곱
SELECT POWER(2,10)
FROM DUAL;

