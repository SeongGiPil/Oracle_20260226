SELECT *
FROM EMP E
INNER JOIN(
SELECT DEPTNO,AVG(SAL) AS AVG_SAL
FROM EMP
GROUP BY DEPTNO
)T ON E.DEPTNO=T.DEPTNO
WHERE SAL>AVG_SAL;

