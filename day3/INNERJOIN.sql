--4.본인 학과의 시험평균점수보다 높은 시험점수를 가진 학생이 학번,이름,평균점수
SELECT S.STU_NO,STU_NAME.ENR_GRADE
FROM STUDENT S
INNER JOIN ENROL E ON S.STU__NO=E.STU_NO
INNER JOIN(
SELECT AVG(ENR_GRADE)
FROM STUDENT S
INNER JOIN ENROL E ON S.STU__NO=E.STU_NO
GROUP BY STU_DEPT
)T ON S.STU_DEPT=T.STU_DEPT
GROUP BY S.STU_NO,STU_NAME,AVG_GRADE
HAVING AVG(ENR_GRADE)> AVG_GRADE;

